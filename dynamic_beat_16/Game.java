		 g.drawImage(noteRouteLineImage, 740, 0, null);
		 g.drawImage(noteRouteLineImage, 844, 0, null);
		 g.drawImage(noteRouteLineImage, 948, 0, null);
		 g.drawImage(noteRouteLineImage, 1052, 0, null);
		 g.drawImage(gameInfoImage, 0, 900, null);
		 g.drawImage(judgmentLineImage, 0, 820, null);
		 for(int i = 0; i < noteList.size(); i++)
		 {
			 Note note = noteList.get(i);
			 if(note.getY() > 880) {
				 judgeImage = new ImageIcon(Main.class.getResource("../images/Miss.png")).getImage();
				 combo = "0";
			 }
			 if(!note.isProceeded()) {
				 noteList.remove(i);
				 i--;
			 }
			 else {
				 note.screenDraw(g);
			 }
		 }
		 g.setColor(Color.white);
		 g.setRenderingHint(RenderingHints.KEY_TEXT_ANTIALIASING,
				 RenderingHints.VALUE_TEXT_ANTIALIAS_ON);
		 g.setColor(Color.WHITE);
		 g.setFont(new Font("Arial", Font.BOLD, 30));
		 g.drawString(titleName, 20, 940);
		 g.drawString(difficulty, 1190, 940);
		 
		 if (combo != "0") {					//콤보 이미지
			 g.setColor(Color.YELLOW);
			 g.setFont(new Font("Elephant", Font.BOLD, 60));
			 g.drawString(combo + " Combo !", 510, 500);
		 }
		 
		 
		 g.setFont(new Font("Arial", Font.PLAIN, 26));
		 g.setColor(Color.DARK_GRAY);
		 g.drawString("S", 270, 850);
		 g.drawString("D", 374, 850);
		 g.drawString("F", 478, 850);
		 g.drawString("Space Bar", 580, 850);
		 g.drawString("J", 784, 850);
		 g.drawString("K", 889, 850);
		 g.drawString("L", 993, 850);
		 g.setColor(Color.LIGHT_GRAY);
		 g.setFont(new Font("Elephant", Font.BOLD, 30));
		 g.drawString(Integer.toString(score), 565, 940);
		 g.drawImage(blueFlareImage, 160, 350, null);
		 g.drawImage(judgeImage, 465, 550, null);
		 g.drawImage(keyPadSImage, 228, 860, null);
		 g.drawImage(keyPadDImage, 332, 860, null);
		 g.drawImage(keyPadFImage, 436, 860, null);
